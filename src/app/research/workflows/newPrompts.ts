function safeJson(value: unknown) {
  return JSON.stringify(value, null, 2);
}

/**
 * A/B/C 信息丰富度评级前言 -- 强制 agent 先评估资料丰富度，按评级调整研究深度。
 */
export function informationRichnessPreamble() {
  return `## 信息丰富度自评（先做这一步）
在开始分析前，先给本公司评级：
- A 级（资料充分）：被广泛覆盖。专注反共识--"聪明人为什么不买？"，不要重复市场共识。
- B 级（资料适中）：有缺口。每个推算指标标注置信度（高/中/低），不要伪装确定性。
- C 级（资料稀缺）：几乎无覆盖。切"第一性原理模式"--只答 4 个核心问题（做什么/赚什么/凭什么/十年后在不在），不要用框架伪装完整性。

在报告开头标注你的评级和理由。`;
}

/**
 * 组合上下文块
 */
export function portfolioContextBlock(portfolioContext?: PortfolioResearchContext) {
  if (!portfolioContext) return "";
  return [
    "## 用户授权的组合上下文",
    "以下 JSON 由 AssetMate 在用户浏览器本地生成，包含用户全部持仓。单项原币金额必须结合 currency 解读；组合比较、权重和汇总只使用 marketValueInBase/costBasisInBase 与 baseCurrency，不得把不同币种的原始金额直接相加：",
    "```json",
    safeJson(portfolioContext),
    "```",
  ].join("\n");
}

